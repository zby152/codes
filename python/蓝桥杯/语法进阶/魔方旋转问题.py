import copy

colors = ["绿", "蓝", "红", "橙", "白", "黄"]
# colors=[1,2,3,4,5,6]

M = [[1, 3, 5], [1, 4, 5], [2, 3, 5], [2, 4, 5], [1, 3, 6], [1, 4, 6], [2, 3, 6], [2, 4, 6]]


def f_x():
    global M
    M_ = copy.deepcopy(M)
    M_[0][0] = M[1][0]
    M_[0][1] = M[1][2]
    M_[0][2] = M[1][1]

    M_[1][0] = M[5][0]
    M_[1][1] = M[5][2]
    M_[1][2] = M[5][1]

    M_[4][0] = M[0][0]
    M_[4][1] = M[0][2]
    M_[4][2] = M[0][1]

    M_[5][0] = M[4][0]
    M_[5][1] = M[4][2]
    M_[5][2] = M[4][1]
    M = copy.deepcopy(M_)


def f_y():
    global M
    M_ = copy.deepcopy(M)
    M_[0][0] = M[4][2]
    M_[0][1] = M[4][1]
    M_[0][2] = M[4][0]

    M_[2][0] = M[0][2]
    M_[2][1] = M[0][1]
    M_[2][2] = M[0][0]

    M_[4][0] = M[6][2]
    M_[4][1] = M[6][1]
    M_[4][2] = M[6][0]

    M_[6][0] = M[2][2]
    M_[6][1] = M[2][1]
    M_[6][2] = M[2][0]
    M = copy.deepcopy(M_)


def f_z():
    global M
    M_ = copy.deepcopy(M)
    M_[0][0] = M[2][1]
    M_[0][1] = M[2][0]
    M_[0][2] = M[2][2]

    M_[1][0] = M[0][1]
    M_[1][1] = M[0][0]
    M_[1][2] = M[0][2]

    M_[2][0] = M[3][1]
    M_[2][1] = M[3][0]
    M_[2][2] = M[3][2]

    M_[3][0] = M[1][1]
    M_[3][1] = M[1][0]
    M_[3][2] = M[1][2]
    M = copy.deepcopy(M_)


fuc = input()
for do_f in fuc:
    if do_f == "x":
        f_x()
    if do_f == "y":
        f_y()
    if do_f == "z":
        f_z()

for color in M[0]:
    print(colors[color - 1], end="")
